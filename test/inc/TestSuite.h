#ifndef DELAUNAY_TESTSUITE_H
#define DELAUNAY_TESTSUITE_H

/***********************************************************************************************************************
* Includes
***********************************************************************************************************************/
#include "TestData.h"

#include <vector>


/***********************************************************************************************************************
* Defines
***********************************************************************************************************************/
#define NUM_POINTS_3                (3)
#define NUM_POINTS_10               (10)
#define NUM_POINTS_1K               (1000)
#define NUM_POINTS_10K              (10000)
#define NUM_POINTS_100K             (100000)
#define NUM_POINTS_1M               (1000000)

#define NUM_ITERATIONS_10           (10)
#define NUM_ITERATIONS_100          (100)
#define NUM_ITERATIONS_1000         (1000)


/***********************************************************************************************************************
* Public method definitions
***********************************************************************************************************************/
class TestSuite
{
    /*******************************************************************************************************************
    * Class members
    *******************************************************************************************************************/
    string strName;
    vector<TestData> vTests;

public:
    /*******************************************************************************************************************
    * Public methods declarations
    *******************************************************************************************************************/
    TestSuite() = default;
    explicit TestSuite(string &strNameIn) : strName(strNameIn) {};

    void add(TestData &test) { vTests.push_back(test); }

    /*******************************************************************************************************************
    * Getters/Setters
    *******************************************************************************************************************/
    const string &getName() const { return strName; }
    size_t getNumTests() { return vTests.size(); }
    TestData &getTest(size_t szIdx) { return vTests.at(szIdx); }
};


#endif //DELAUNAY_TESTSUITE_H
